package com.szrt.java.zzm.java20161222;

public class HomeWork {

	public static void main(String[] args) {
		//打印出整型类型Byte的最大最小值
		System.out.println("Byte最小值为:"+Byte.MIN_VALUE);
		System.out.println("Byte最大值为:"+Byte.MAX_VALUE);
		//打印出整型类型short的最大最小值
		System.out.println("Short的最小值为:"+Short.MIN_VALUE);
		System.out.println("Short的最大值为:"+Short.MAX_VALUE);
		//打印出整型类型int的最大最小值
		System.out.println("Integer的最小值为:"+Integer.MIN_VALUE);
		System.out.println("Integer的最大值为:"+Integer.MAX_VALUE);
		//打印出整型类型long的最大最小值
		System.out.println("Long的最小值为:"+Long.MIN_VALUE);
		System.out.println("Long的最大值为:"+Long.MAX_VALUE);
		System.out.println();
		//打印出字符型char的最大最小值数字
		char max=Character.MAX_VALUE;
		char min=Character.MIN_VALUE;
		int CharMax=max;
		int CharMin=min;
		System.out.println("Character的最小值为:"+CharMin);
		System.out.println("Character的最大值为:"+CharMax);
		
		
	}
}
